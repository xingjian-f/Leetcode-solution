class Solution(object):
    def compareVersion(self, version1, version2):
        """
        :type version1: str
        :type version2: str
        :rtype: int
        """
        version1 = version1.split('.')
        version2 = version2.split('.')
        for i in range(len(version1))[::-1]:
        	if int(version1[i]) == 0:
        		del version1[i]
        	else:
        		break
        for i in range(len(version2))[::-1]:
        	if int(version2[i]) == 0:
        		del version2[i]
        	else:
        		break
        for i in range(min(len(version1), len(version2))):
        	a = int(version1[i])
        	b = int(version2[i])
        	if a == b:
        		continue
        	elif a > b:
        		return 1
        	else:
        		return -1
        if len(version1) > len(version2):
        	return 1
        elif len(version1) < len(version2):
        	return -1
        else:
        	return 0


